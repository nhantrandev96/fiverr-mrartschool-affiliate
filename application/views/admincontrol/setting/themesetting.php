    url:'<?= base_url("admincontrol/editor_get_file") ?>',

    type:'POST',

    dataType:'json',

    data:{path: $this.attr("data-path")},

    beforeSend:function(){$this.btn("loading");},

    complete:function(){$this.btn("reset");},

